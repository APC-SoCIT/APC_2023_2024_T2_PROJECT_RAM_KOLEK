            <p>RECORDS KEEPING and RETENTION</p>
            <ol>
                <li>IRR of the DPA of 2012, Rule IV. Data Privacy Principles, Sec. 19: “Personal Data shall not be retained longer than necessary.</li>
                <li style="margin-top: 8px;">The personnel records and data shall be stored inside the HR office or an approved secure storage facility off-site.</li>
                <li style="margin-top: 8px;">Official personnel and other records shall only be retained and stored in the HR Office for a maximum of five (5) years. All documents, which have collected and stored in the HR Office beyond the prescribed period, shall be disposed immediately</li>
                <li style="margin-top: 8px;">The student records shall be stored at the Registrar’s office. Physical and digital permanent student records shall be retained/secured perpetually while other supporting documents shall be discarded a year after they graduate. Likewise, physical and digital supporting documents of AWOL students will only be retained within 10 years.
                </li>
                <li style="margin-top: 8px;">All documents, which have collected and stored beyond the prescribed period shall be disposed immediately.
                </li>
                <li style="margin-top: 8px;">The disposal of records and data shall be performed by the concerned office with the use of approved disposal methods.</li>
            </ol>
            
            <br>
            <br>
            <p>DISCLOSURE OF DATA AND INFORMATION</p>
            <ol>
                <li style="margin-top: 8px;">IRR of the DPA of 2012, Rule IV. Data Privacy Principles, Sec. 20: “General principles for Data Sharing. Further Processing of Personal Data collected from a party other than the Data Subject shall be allowed under any of the conditions stipulated in DPA of 2012.</li>
                <li style="margin-top: 8px;">Only authorized personnel of each unit, who was assigned by the Executive Director, are allowed to collect, handle, and view personal records and other pertinent data, information, or documents, which are in any way related to any previously or currently employed faculty or staff of the College. Proper handling of data and records should always be practiced by authorized personnel.</li>
                <li style="margin-top: 8px;">Each unit of APC has the right to use data and information collected for whatever it deems necessary and vital to its official business and development only as approved by the President.</li>
                <li style="margin-top: 8px;">Disclosure of data and information shall only be done through proper and official means with approval of the Executive Director.</li>
            </ol>
